#Take a number input from the user and display the result of some mathematical calculations

import math
a = float(input('Enter a number: '))

print('Square of Number: ', a**2)
print('Square root of Number: ', a**(1/2))
print("Exponent Value =", math.exp(a))
print("Log Base 10 =", math.log10(a))
print("Power of 3 =", a**3)
print("Power of 4 =", a**4)
print("Power of 5 =", a**5)